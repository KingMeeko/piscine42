int	ft_sqrt(int nb)
{
	int	i;
	int	sq;

	i = 1;
	sq = 1;
	if (nb == 0)
		return (0);
	while (i * i < nb)
	{
		i++;
	}
	if ((nb % i) == 0)
		return (i);
	else if (i >= 46341)
		return (0);
	else
		return (0);
}
