#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	*tab;
	int	i;

	i = 0;
	if (min >= max)
		return (NULL);
	tab = malloc(sizeof(int) * (max - min));
	while (min != max)
	{
		tab[i] = min;
		i++;
		min++;
	}
	return (tab);
}
